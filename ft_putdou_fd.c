#include "libft.h"

void	ft_putdou_fd(double d, int fd)
{
	ft_putnbr_fd((int)d, fd);
	write(1, ".", 1);
	d = (d - (int)d) * 100000;
	ft_putnbr_fd((int)d, fd);
}
