#include "libft.h"

static void	rec(int nbr)
{
	if (nbr < 10)
		ft_putchar ('0' + nbr);
	else
	{
		rec (nbr / 10);
		rec (nbr % 10);
	}
}

void		ft_putnbr(int n)
{
	if (n == -2147483648)
		write (1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write (1, "-", 1);
			n = 0 - n;
		}
		rec (n);
	}
}
